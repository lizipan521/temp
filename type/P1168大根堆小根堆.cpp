#include<bits/stdc++.h>
typedef long long ll;
//======================================
const int maxn = 1e5 + 10;
int a[maxn], n;
std::priority_queue<int, std::vector<int>, std::greater<int> >q1;  //小根堆
std::priority_queue<int>q2; //大根堆

int main(int argc, char const *argv[])
{
#ifdef LOCAL
    freopen("in.in", "r", stdin);
    freopen("out.out", "w", stdout);
#endif
    //======================================
    scanf("%d", &n);
    for(int i = 1; i <= n; i++) scanf("%d", &a[i]);
    int mid = a[1];
    printf("%d\n", a[1]);
    for (int i = 2; i <= n + 1>>1; i++){
        int m = i - 1;
        for (int j = m<<1; j < i <<1; j++){
            if (a[j] < mid) q2.push(a[j]);
            else q1.push(a[j]);
        }
        while(q1.size() != q2.size()){
            if(q1.size() > q2.size()){
                q2.push(mid);
                mid = q1.top();
                q1.pop();
            }
            else {
                q1.push(mid);
                mid = q2.top();
                q2.pop();
            }
        }
        printf("%d\n", mid);
    }
    //======================================
    return 0;
}
