#include<bits/stdc++.h>
typedef long long ll;
//======================================
const int maxn = 1e5 + 5;
int a[maxn], f[maxn], n, len;
bool cmp(int x, int y){return x > y;}
int main(int argc, char const *argv[])
{
#ifdef LOCAL
    freopen("in.in", "r", stdin);
    freopen("out.out", "w", stdout);
#endif
    //clock_t c1 = clock();
    //======================================
    while(scanf("%d", &a[n]) != EOF) n++;
    f[++len] = a[0];
    //printf("%d\n", n);
    for (int i = 1; i < n; i++){
        if (a[i] <= f[len]) f[++len] = a[i];
        else {
            *std::upper_bound(f + 1, f + len + 1, a[i], cmp) = a[i];//最长单调不上升序列
        }
    }
    printf("%d\n", len);
    memset(f, 0, sizeof(f));
    len = 0;
    f[++len] = a[0];
    for (int i = 1; i < n; i++){
        if (a[i] > f[len]) f[++len] = a[i];
        else {
            *std::lower_bound(f + 1, f + 1 + len, a[i]) = a[i];//最长单调上升序列
        }
    }
    printf("%d", len);
    //======================================
    //std::cerr << "Time:" << clock() - c1 << "ms" << std::endl;
    return 0;
}
